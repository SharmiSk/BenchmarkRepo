package com.intra.benchmark.app.dto;

/*
 * @author :Sharmila
 */

public class Student {
	
	private int Id;
	
	private String Name;
	
	private String Subject;
	
	private int Age;
	

	public int getId() {
		return Id;
	}

	public void setId(int id) {
		Id = id;
	}

	public String getName() {
		return Name;
	}

	public void setName(String name) {
		Name = name;
	}

	
	public String getSubject() {
		return Subject;
	}

	public void setSubject(String subject) {
		Subject = subject;
	}

	public int getAge() {
		return Age;
	}

	public void setAge(int age) {
		Age = age;
	}
	
}
